                <h4>Tema para chás de bebê</h4>
                <h5>Antes de começar a criar convites e decorações, é útil organizá-los em um tema. O tema deve refletir a personalidade e as preferências da futura mamãe, e também podem ser centralizado no novo bebê. Por exemplo, se você não sabe o sexo do bebê, poderia criar um tema com gravatas-borboletas e borboletas ou com um safári. Se você sabe que será uma menina, então um tema de fadas cor de rosa, com um bolo em forma de castelo seria ótimo!</h5>

